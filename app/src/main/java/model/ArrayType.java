package model;

/**
 * Represents the types of arrays based on predefined categories.
 * Each category indicates a specific configuration or level.
 */
public enum ArrayType {
    /**
     * Represents a low-level array type, variant 1.
     */
    LOW_1,
    
    /**
     * Represents a low-level array type, variant 2.
     */
    LOW_2,
    
    /**
     * Represents a low-level array type, variant 3.
     */
    LOW_3,
    
    /**
     * Represents a medium-level array type, variant 1.
     */
    MEDIUM_1,
    
    /**
     * Represents a medium-level array type, variant 2.
     */
    MEDIUM_2,
    
    /**
     * Represents a medium-level array type, variant 3.
     */
    MEDIUM_3,
    
    /**
     * Represents a medium-level array type, variant 4.
     */
    MEDIUM_4,
    
    /**
     * Represents a high-level array type, variant 1.
     */
    HIGHT_1,
    
    /**
     * Represents a high-level array type, variant 2.
     */
    HIGHT_2
}
